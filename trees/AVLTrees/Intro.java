package trees.AVLTrees;

public class Intro 
{
	
	/*
	     1) AVL Tree is balanced height BST
	     
	     2)  balanced height ==> for each node , |hl - hr| <= 1
	     
	               where hr = height of right subtree and
	               hl = height of left subtree
	 
	     3) If tree is unbalanced , we perform rotation on tree to make it balanced
	     
	     4) Based on insertion , if node is unbalanced , rotation are classified as
	          LL   ---> Clockwise rotated
	          RR   ---> Anti-clockwise
	          LR   ---> LR rotated
	          RL   ---> RL rotated
	  
	 
	 
	 */

}
